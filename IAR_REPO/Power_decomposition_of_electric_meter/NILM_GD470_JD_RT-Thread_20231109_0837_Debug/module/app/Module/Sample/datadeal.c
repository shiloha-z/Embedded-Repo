/**
 * @brief 从rawdate中获取电压数据存储到voltagedatebuff中
 * 
 * @param rawdate 原始数据指针
 * @param voltagedatebuff 存储电压数据的指针
 */
void get_voltage(void *rawdate,float *voltagedatebuff)
{
	long ptemp;
	BYTE *r_date, temp1, i = 0;
	WORD bias;
	r_date = (BYTE *)rawdate;
	temp1 = CHMAX*4;

	for (i = 0; i < SAMPLE_NUM; i++){
		bias = temp1 * i;
		ptemp = (r_date[bias+3]<<24)+(r_date[bias+2]<<16)+(r_date[bias+1]<<8)+(r_date[bias]);
		*(voltagedatebuff+i) = (float)(ptemp * SysPara.big_adjust_para.U_factor[0]);
	}
}

/**
 * @brief 从rawdate中获取电流数据存储到currentdatebuff中
 * 
 * @param rawdate 原始数据指针
 * @param currentdatebuff 存储电流数据的指针
 */
void get_current(void *rawdate,float *currentdatebuff)
{
	long ptemp;
	BYTE *r_date, temp1, i = 0;
	WORD bias;
	r_date = (BYTE *)rawdate;
	temp1 = CHMAX*4;

	for (i = 0; i < SAMPLE_NUM; i++){
		bias = temp1 * i + 4;
		ptemp = (r_date[bias+3]<<24)+(r_date[bias+2]<<16)+(r_date[bias+1]<<8)+(r_date[bias]);
		*(currentdatebuff + i) = (float)(ptemp * SysPara.big_adjust_para.I_factor[0]);
	}
}

